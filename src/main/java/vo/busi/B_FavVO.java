package vo.busi;

public class B_FavVO {

	private int b_fav_seq;
	private int m_seq;
	private int busi_seq;
	
	public B_FavVO() {
	}

	public B_FavVO(int b_fav_seq, int m_seq, int busi_seq) {
		this.b_fav_seq = b_fav_seq;
		this.m_seq = m_seq;
		this.busi_seq = busi_seq;
	}

	public int getB_fav_seq() {
		return b_fav_seq;
	}

	public void setB_fav_seq(int b_fav_seq) {
		this.b_fav_seq = b_fav_seq;
	}

	public int getM_seq() {
		return m_seq;
	}

	public void setM_seq(int m_seq) {
		this.m_seq = m_seq;
	}

	public int getBusi_seq() {
		return busi_seq;
	}

	public void setBusi_seq(int busi_seq) {
		this.busi_seq = busi_seq;
	}

	
	
	
	
	
	
}
