package vo.member;


public class CollectionVO {

	private int collection_seq;
	private int m_seq;
	private int seller_seq;
	
	public CollectionVO() {

	}

	public int getCollection_seq() {
		return collection_seq;
	}

	public void setCollection_seq(int collection_seq) {
		this.collection_seq = collection_seq;
	}

	public int getM_seq() {
		return m_seq;
	}

	public void setM_seq(int m_seq) {
		this.m_seq = m_seq;
	}

	public int getSeller_seq() {
		return seller_seq;
	}

	public void setSeller_seq(int seller_seq) {
		this.seller_seq = seller_seq;
	}
	
	
}
	
